#!/usr/bin/env node
// Reads every preset JSON from the cloned Pulsar repo and emits a single
// bundle (src/ui/presets-data/index.ts) the UI can import without touching disk at runtime.
import { readFileSync, readdirSync, writeFileSync, mkdirSync } from 'fs';
import { join, dirname, basename } from 'path';
import { fileURLToPath } from 'url';

const here = dirname(fileURLToPath(import.meta.url));
const PULSAR_ROOT = join(here, '..', '..', 'pulsar');
const OUT = join(here, '..', 'src', 'ui', 'presets-data', 'index.ts');

const sources = [
  { dir: join(PULSAR_ROOT, 'docs/src/content/docs/assets/presets'), category: 'user', platform: null },
  { dir: join(PULSAR_ROOT, 'docs/src/content/docs/assets/systemPresets/ios'), category: 'system', platform: 'ios' },
  { dir: join(PULSAR_ROOT, 'docs/src/content/docs/assets/systemPresets/android'), category: 'system', platform: 'android' }
];

const all = [];
for (const { dir, category, platform } of sources) {
  const files = readdirSync(dir).filter((f) => f.endsWith('.json')).sort();
  for (const f of files) {
    const data = JSON.parse(readFileSync(join(dir, f), 'utf8'));
    all.push({
      id: `${category}${platform ? '-' + platform : ''}-${basename(f, '.json')}`,
      category,
      platform,
      data
    });
  }
}

mkdirSync(dirname(OUT), { recursive: true });
const body = `// AUTO-GENERATED by scripts/build-preset-index.mjs — do not edit.
import type { CatalogEntry } from '../../shared/types';
const PRESETS: CatalogEntry[] = ${JSON.stringify(all, null, 0)};
export default PRESETS;
`;
writeFileSync(OUT, body);
console.log(`Wrote ${all.length} presets to ${OUT}`);
